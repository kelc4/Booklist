package ui;

import javax.swing.*;

public class BookListAppGUI {

    public void runGUI() {
        JFrame frame = new JFrame();
        frame.setTitle("BookList Application");
        frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE); //makes program stop running after closing frame
        frame.setResizable(false); //prevents frame from being resized
        frame.setSize(420, 420); //x, y dimensions of frame
        frame.setVisible(true); //makes frame visible



    }

}
